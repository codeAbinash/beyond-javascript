const arr = new Array() // Empty Array
console.log(arr) // [], empty Array
console.log(arr.length) // 0

const colors = Array('Red', 'Pink') // Pass string values as arguments
console.log(colors) // [ 'Red', 'Pink' ]
console.log(colors.length) // 2


const names = Array('Raju', 'Babu Rao', 'Shyam') // Pass string values as arguments
// Creates new array without using new keyword
console.log(names) // [ 'Raju', 'Babu Rao', 'Shyam' ]
console.log(names.length) // 3